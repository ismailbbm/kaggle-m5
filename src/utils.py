def running_mean(self,x, N):
    cumsum = np.cumsum(np.insert(x, 0, 0))
    end_part = (cumsum[N:] - cumsum[:-N]) / float(N)
    beg_part = np.cumsum(x[:N-1])/np.arange(1,N)
    return np.concatenate([beg_part,end_part])